    private void enterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterActionPerformed

        //RESTRICTIONS
        if (USER.getText().equals("") && password.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Favor ingresar un usuario y contrase単a.");
        } 
        else if (USER.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Favor ingresar un usuario.");
        } 
        else if (password.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Favor ingresar una contrase単a.");
        } 
                
        String usuario = USER.getText();
        String contrase単a = password.getText();
        
        //SESION EXITOSA
        if (userDatabase.login(usuario, contrase単a)) {
            JOptionPane.showMessageDialog(null, "Inicio de sesion exitoso.");
            MENU_HOME menu = new MENU_HOME();
            menu.setVisible(true);
            this.dispose();
        }else{
            
            //NO SE UNIO
            Object[] opciones = {"Intentar de nuevo", "Cambiar cuenta"};
            int respuesta = JOptionPane.showOptionDialog(null, 
                "Desea intentar de nuevo o quiere ir a cambiar cuenta?", 
                "Confirmar", 
                JOptionPane.YES_NO_OPTION, 
                JOptionPane.QUESTION_MESSAGE, 
                null, 
                opciones, 
                opciones[0]);

            //INTENTAR DE NUEVO
            if (respuesta == JOptionPane.YES_OPTION) {
                    USER.setText("");
                    password.setText("");                    
            }

            //MANDAR A CREAR CUENTA
            else if (respuesta == JOptionPane.NO_OPTION) {
                Crear_Cuenta crearCuenta = new Crear_Cuenta(this.userDatabase);
                crearCuenta.setVisible(true);
                this.dispose(); 
            }            
        }        
    }//GEN-LAST:event_enterActionPerformed

